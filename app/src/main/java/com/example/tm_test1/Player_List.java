package com.example.tm_test1;

public class Player_List {

    private String player_name;
    private int player_img;

    public Player_List(String player_name, int player_img) {
        this.player_name = player_name;
        this.player_img = player_img;
    }

    public String getPlayer_name() {
        return player_name;
    }

    public int getPlayer_img() {
        return player_img;
    }


}
